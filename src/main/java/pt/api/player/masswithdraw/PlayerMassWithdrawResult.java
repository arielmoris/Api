package pt.api.player.masswithdraw;

public class PlayerMassWithdrawResult {
	
	
	public PlayerMassWithdrawResult() {
		super();
		this.playername = new String();
		this.withdraw = new PlayerMassWithdrawWithdraw();
	}
	
	private String playername;
	private PlayerMassWithdrawWithdraw withdraw;
	public String getPlayername() {
		return playername;
	}
	public void setPlayername(String playername) {
		this.playername = playername;
	}
	public PlayerMassWithdrawWithdraw getWithdraw() {
		return withdraw;
	}
	public void setWithdraw(PlayerMassWithdrawWithdraw withdraw) {
		this.withdraw = withdraw;
	}
	@Override
	public String toString() {
		return "PlayerMassWithdrawResult [playername=" + playername
				+ ", withdraw=" + withdraw + "]";
	}
	
	
	
}
